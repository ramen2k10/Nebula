package com.demo.nebula.table;


public class TestModel {
    private String testCaseName;
    private String testCaseTypes;
    private String partTestSet;
    private String testCaseDescription;

    public String getTestCaseName() {
        return testCaseName;
    }
    public void setTestCaseName(String testCaseName) {
        this.testCaseName = testCaseName;
    }
    public String getTestCaseTypes() {
        return testCaseTypes;
    }
    public void setTestCaseTypes(String testCaseTypes) {
        this.testCaseTypes = testCaseTypes;
    }
    public String getPartTestSet() {
        return partTestSet;
    }
    public void setPartTestSet(String partTestSet) {
        this.partTestSet = partTestSet;
    }
    public String getTestCaseDescription() {
        return testCaseDescription;
    }
    public void setTestCaseDescription(String testCaseDescription) {
        this.testCaseDescription = testCaseDescription;
    }
}

